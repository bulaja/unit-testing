def add(a, b):
    """
    Add two numbers.

    Args:
        a (float or int): The first number.
        b (float or int): The second number.

    Returns:
        float or int: The result of adding a and b.
    """
    return a + b


def subtract(a, b):
    """
    Subtract two numbers.

    Args:
        a (float or int): The first number.
        b (float or int): The second number.

    Returns:
        float or int: The result of subtracting b from a.
    """
    return a - b